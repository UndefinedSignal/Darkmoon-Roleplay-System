function RPSCoreFramework:SetEnchTexture(frame, i)
	local EnchTexture = frame:CreateTexture("texture", "OVERLAY");
	EnchTexture:SetPoint("CENTER");
	EnchTexture:SetTexture("Interface\\AddOns\\RPSDarkmoon\\resources\\enchants\\ench"..i..".blp");
	EnchTexture:SetSize(256, 64);
	EnchTexture:SetDrawLayer("OVERLAY", 0);
	EnchTexture:SetPoint("TOPLEFT", frame.IDLabel ,"TOPRIGHT", 8, 12);
	EnchTexture:SetPoint("BOTTOMRIGHT", frame ,"RIGHT", -15, -4);
	EnchTexture:Show();
end


function RPSCoreFramework:InitializeEnchantButtons()
	local mainframe = _G["DarkmoonWeaponEnchantsFrameContentCommandsSlider1Child"];
	for i = 1, #RPSCoreFramework.Enchants do
        local button = CreateFrame("Button", "DarkmoonEnchantButton"..i, mainframe, "RPSEnchantButton");
        if i == 1 then
        	button:SetPoint("TOPLEFT", mainframe, 3, -1);
    	elseif i <= 2 then
    		button:SetPoint("LEFT", _G["DarkmoonEnchantButton"..i-1], "RIGHT", 0, 0);
    	else
    		button:SetPoint("TOP", _G["DarkmoonEnchantButton"..i-2], "BOTTOM", 0, 0);
    	end
    	button.IDLabel:SetText(i);
    	button.TitleLabel:SetText(RPSCoreFramework.Enchants[i][3]);
    	button.Title = RPSCoreFramework.Enchants[i][3];
    	button.Description = RPSCoreFramework.Enchants[i][4];
    	RPSCoreFramework:SetEnchTexture(button, i);
    	button:Show();
	end
end